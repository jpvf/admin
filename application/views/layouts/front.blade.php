        <br><h1 style='position:relative !important;font-weight:bold;color:#333'>PDF</h1>

        <div id="img1"></div>

        <br><h2><strong>¿Por qué es importante generar correctamente un pdf?</strong>

        <br><br>Es muy importante crear archivos PDF profesionales para los procesos de impresión, pues la calidad del producto final depende de que las imágenes hayan sido correctamente digitalizadas, que tengan la resolución adecuada y que estén en el espacio de color CMYK con la debida adaptación de color.

        <br><br><a href="generar-un-buen-pdf-para-impresion.html"><strong><u>¿Cómo generar un buen PDF listo para impresión?</u></strong></a></h2>

        <br><h1 style='position:relative !important;font-weight:bold;color:#333'>ARCHIVOS</h1>

        <div id="img1"></div>

         <br><h2><strong>¿Qué tipo de archivos debo enviar?</strong>

        <br><br>Recibimos archivos PDF debidamente generados y en caso de no tenerlo; podra contactarnos para que lo hagamos por usted, debe entregarnos sus archivos abiertos (Photoshop, Illustrator, Freehand, InDesing) y realizaremos su PDF. Es importante que sepa, que esto será un servicio adicional que tiene un costo.

        <br><br><strong>¿Por qué pueden generar errores mis archivos?</strong>

        <br><br>Los principales problemas con los archivos son los siguientes:<br><br>

            1.  &nbsp;&nbsp;&nbsp;Resolución de imagen muy baja.<br>

            2.  &nbsp;&nbsp;&nbsp;Fuentes sin integrar al archivo.<br>

            3.  &nbsp;&nbsp;&nbsp;Espacio de color erróneo.<br>

            4.  &nbsp;&nbsp;&nbsp;Información incorrecta sobre refile o sangrado.<br>

            5.  &nbsp;&nbsp;&nbsp;Color especial con nombre equivocado o convertido a policromía.<br>

            6.  &nbsp;&nbsp;&nbsp;Demasiada compresión de imagen.<br>

            7.  &nbsp;&nbsp;&nbsp;Información incorrecta sobre el tamaño de la página.<br>

            8.  &nbsp;&nbsp;&nbsp;Problemas con objetos transparentes.

        <br><br><strong>¿Por qué en algunos impresos el color negro se ve bandeado o gris?</strong>

        <br><br>Por sí sola, la tinta negra nunca es lo bastante intensa. Por esto es recomendable que los negros tengan una base de cyan, puede componerlo con un 40% de cyan y 100% de black.

        <br><br><strong>¿Qué debo hacer si no se como envíar mi trabajo correctamente para imprimir?</strong>

         <br><br>Zetta comunicadores posee un teléfono de contacto para atención al cliente de 8:00 a.m. a 6:00 p.m. de lunes a viernes (3599000 ext. 205) o vía e-mail contacto@zetta.com donde estaremos atentos para solucionar antes de 12 horas todas sus dudas.

         <br><br><strong>¿Qué es 4x0 y 4x4?</strong>

         <br><br>Estos números indican la cantidad de tintas que lleva el impreso por cada lado del papel, son 4 tintas: Cyan Magenta Yellow Black, así que 4x0 indicaría que es policromía por una cara y sin impresión por la otra, y 4x4 que es policromía por las dos caras.</h2>

